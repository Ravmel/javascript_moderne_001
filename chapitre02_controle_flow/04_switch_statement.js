const mention = "D";

switch (mention) {
    case "A":
        console.log("Vous avez obtenu un A");
        break;

    case "B":
        console.log("Vous avez obtenu un B");
        break;

    case "C":
        console.log("Vous avez obtenu un C");
        break;

    case "D":
        console.log("Vous avez obtenu un D");
        break;

    default :
        console.log("Vous n'avez pas de mention valide");
}